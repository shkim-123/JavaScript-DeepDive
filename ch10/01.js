// [10.1 객체란?]
// js 는 객체(object) 기반의 프로그래밍 언어
// 원시 값은 변경 불가능한 값(immutable value), 객체는 변경 가능한 값(mutable value)

// 객체는 0개 이상의 프로퍼티로 구성된 집합, 프로퍼티는 키(key), 값(value)로 구성됨
// js 에서 사용할 수 있는 모든 값은 프로퍼티 값이 될 수 있음
// 함수도 프로퍼티 값으로 사용할 수 있으며, 일반 함수와 구분하기 위해 메서드(method)라 부름

// 객체는 프로퍼티와 메서드로 구성된 집합체
// 프로퍼티 : 객체의 상태를 나타내는 값(value)
// 메서드 : 프로퍼티(상태 데이터)를 참조하고 조작할 수 있는 동작(behavior)
